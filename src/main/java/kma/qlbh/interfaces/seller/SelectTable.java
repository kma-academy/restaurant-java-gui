    TableDao tableDao = new TableDao();
    ArrayList<Table> tables;

    public SelectTable() {
        initComponents();
        setLocationRelativeTo(null);
        try {
            tables = tableDao.getAll();
            int totalTable = tables.size();
            if (totalTable == 0) {
                throw new Exception("Chưa có bàn nào");
            }
            int maxRow = 10, maxCollumn = (int) Math.ceil((double) totalTable / 10);
            panelContent.setPreferredSize(new Dimension(sloveSize(maxRow), sloveSize(maxCollumn)));
            panelBottom.setPreferredSize(new Dimension(sloveSize(maxRow), 60));
            this.setPreferredSize(new Dimension(sloveSize(maxRow), sloveSize(maxCollumn) + 60));
            renderTable();
        } catch (Exception ex) {
            this.dispose();
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private int sloveSize(int items) {
        return items * 105 + 5;
    }

    private void renderTable() {
        panelContent.removeAll();
        for (Table table : tables) {
            panelContent.add(new TablePane(table));
        }
        panelContent.updateUI();
    }

